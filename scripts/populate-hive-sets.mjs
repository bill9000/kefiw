// Generates 366 verified Hive sets and writes them into
// src/data/daily-hive-sets.ts.
//
// Algorithm:
//   1. Read public/data/enable.txt
//   2. Find all 7-letter words with exactly 7 distinct letters — these
//      are candidate pangrams.
//   3. Group by 7-letter set (the "pangram signature").
//   4. For each distinct 7-letter signature, keep the shortest pangram
//      plus up to 3 alternatives as reference.
//   5. Score each set by "playable richness" = count of dict words in
//      ENABLE that use only those 7 letters, contain the center, and
//      are ≥4 chars. Reject sets where richness < 15 (too sparse) or
//      > 200 (too noisy / common-letter blob).
//   6. Pick 366 sets, rotating the center letter each day.
//
// Output is deterministic — seeded sort. Re-running the script on the
// same dict produces the same 366 sets.

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');
const DICT_PATH = resolve(ROOT, 'public/data/enable.txt');
const OUT_PATH = resolve(ROOT, 'src/data/daily-hive-sets.ts');
const TARGET_COUNT = 366;
const MIN_RICHNESS = 15;
const MAX_RICHNESS = 200;

function fnv1a(str) {
  let h = 2166136261 >>> 0;
  for (let i = 0; i < str.length; i++) {
    h ^= str.charCodeAt(i);
    h = Math.imul(h, 16777619) >>> 0;
  }
  return h >>> 0;
}

const dict = readFileSync(DICT_PATH, 'utf8')
  .split(/\r?\n/)
  .map((w) => w.trim().toUpperCase())
  .filter((w) => w.length >= 4 && /^[A-Z]+$/.test(w));

console.log(`[hive-gen] loaded ${dict.length} words from ENABLE`);

const bySignature = new Map();
for (const word of dict) {
  if (word.length < 7) continue;
  const unique = new Set(word);
  if (unique.size !== 7) continue;
  const sig = [...unique].sort().join('');
  if (!bySignature.has(sig)) {
    bySignature.set(sig, []);
  }
  bySignature.get(sig).push(word);
}
console.log(`[hive-gen] found ${bySignature.size} distinct 7-letter pangram signatures`);

const wordsByAllowedMask = new Map();
for (const word of dict) {
  if (word.length < 4) continue;
  const unique = new Set(word);
  if (unique.size > 7) continue;
  const sig = [...unique].sort().join('');
  if (!wordsByAllowedMask.has(sig)) wordsByAllowedMask.set(sig, []);
  wordsByAllowedMask.get(sig).push(word);
}

function wordsForSet(signature, center) {
  const sigLetters = new Set(signature);
  let count = 0;
  for (const [otherSig, words] of wordsByAllowedMask) {
    let subset = true;
    for (const ch of otherSig) {
      if (!sigLetters.has(ch)) {
        subset = false;
        break;
      }
    }
    if (!subset) continue;
    if (!otherSig.includes(center)) continue;
    count += words.length;
  }
  return count;
}

const candidates = [];
for (const [sig, pangrams] of bySignature) {
  for (const center of sig) {
    const richness = wordsForSet(sig, center);
    if (richness < MIN_RICHNESS || richness > MAX_RICHNESS) continue;
    const outer = sig.replace(center, '').split('');
    candidates.push({ sig, center, outer, richness, pangrams: pangrams.slice(0, 3) });
  }
}
console.log(`[hive-gen] ${candidates.length} viable (sig, center) pairs after richness filter`);

candidates.sort((a, b) => {
  const ha = fnv1a(a.sig + '|' + a.center);
  const hb = fnv1a(b.sig + '|' + b.center);
  return ha - hb;
});

const selectedSigs = new Set();
const picked = [];
for (const cand of candidates) {
  if (picked.length >= TARGET_COUNT) break;
  if (selectedSigs.has(cand.sig)) continue;
  selectedSigs.add(cand.sig);
  picked.push(cand);
}
console.log(`[hive-gen] picked ${picked.length} distinct signatures`);

if (picked.length < TARGET_COUNT) {
  for (const cand of candidates) {
    if (picked.length >= TARGET_COUNT) break;
    picked.push(cand);
  }
  console.log(`[hive-gen] filled to ${picked.length} by allowing duplicate signatures with different centers`);
}

if (picked.length < TARGET_COUNT) {
  throw new Error(`[hive-gen] not enough viable sets: ${picked.length} < ${TARGET_COUNT}`);
}

const entries = picked.slice(0, TARGET_COUNT).map((c) => ({
  center: c.center,
  outer: c.outer,
  pangrams: c.pangrams,
}));

const header = `// Curated letter sets for Hive, indexed by day-of-year (1..366).
//
// Each set has a center + 6 outer letters = 7 distinct letters. Every set is
// verified to have at least one pangram (word using all 7 letters) present in
// Kefiw's ENABLE dictionary, and passes a "playable richness" filter of
// ${MIN_RICHNESS}..${MAX_RICHNESS} valid words per puzzle.
//
// This file is generated by scripts/populate-hive-sets.mjs. To regenerate,
// run: node scripts/populate-hive-sets.mjs
//
// To override a specific day, replace its entry in place after generation.

import { rngFor, pickInt } from '~/lib/daily-rng';

export interface HiveSet {
  center: string;
  outer: [string, string, string, string, string, string];
  pangrams: string[];
}

export const HIVE_SETS_LENGTH = 366;

`;

const arrayLiteral = entries
  .map((e) => {
    const outer = e.outer.map((l) => `'${l}'`).join(', ');
    const pangrams = e.pangrams.map((p) => `'${p}'`).join(', ');
    return `  { center: '${e.center}', outer: [${outer}], pangrams: [${pangrams}] },`;
  })
  .join('\n');

const footer = `

const CURATED: HiveSet[] = [
${arrayLiteral}
];

if (CURATED.length !== HIVE_SETS_LENGTH) {
  throw new Error(
    \`[daily-hive-sets] expected \${HIVE_SETS_LENGTH} entries, got \${CURATED.length}\`
  );
}

export const CURATED_HIVE_SETS: ReadonlyArray<HiveSet> = CURATED;

export function getHiveSet(dayOfYear: number): HiveSet {
  const idx = ((dayOfYear - 1) % HIVE_SETS_LENGTH + HIVE_SETS_LENGTH) % HIVE_SETS_LENGTH;
  return CURATED[idx];
}
`;

writeFileSync(OUT_PATH, header + footer.replace('${arrayLiteral}', arrayLiteral), 'utf8');
console.log(`[hive-gen] wrote ${OUT_PATH}`);
