function list(items: string[]): string {
  return items.map((item) => `- ${item}`).join('\n');
}

function sourceList(sources: CareSource[]): string {
  return sources.map((source) => `- [${source.label}](${source.href})`).join('\n');
}

function defaultPrimaryCta(category: string): { href: string; label: string } {
  if (category.includes('Medicare')) return { href: '/care/medicare/', label: 'Explore Medicare Tools' };
  if (category.includes('Insurance')) return { href: '/care/insurance/', label: 'Explore Insurance Tools' };
  if (category.includes('Wellbeing')) return { href: '/care/wellbeing/', label: 'Use Wellbeing Tools' };
  if (category.includes('Caregiving')) return { href: '/care/caregiving/', label: 'Explore Caregiving Tools' };
  if (category.includes('Local')) return { href: '/care/guides/', label: 'Explore Care Guides' };
  return { href: '/care/senior-care/', label: 'Explore Senior Care Tools' };
}

